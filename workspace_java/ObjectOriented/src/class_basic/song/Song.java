package class_basic.song;

public class Song {
    String title;
    String artist;
    String album;
    int year;
    String composer;

    public void setTitle(String t){
        title=t;
    }
    public void setArtist(String art){
        artist=art;
    }
    public void setAlbum(String al){
        album=al;
    }
    public void setYear(int y){
        year=y;
    }
    public void setComposer(String com){
        composer=com;
    }
}
